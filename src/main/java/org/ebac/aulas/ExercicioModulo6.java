package org.ebac.aulas;

public class ExercicioModulo6 {

    public void acordar(boolean dormindo){

        // Passo 2: Verifique se você está dormindo
        if (dormindo) {
            // Passo 3: Se estiver dormindo, acorde
            System.out.println("Você está dormindo. Hora de acordar!");

            // Passo 4: Levante-se da cama
            System.out.println("Levante-se da cama.");

        } else {
            // Passo 6: Se você já está acordado, não precisa fazer nada
            System.out.println("Você já está acordado. Não é preciso levantar da cama novamente.");
        }
    }


}
